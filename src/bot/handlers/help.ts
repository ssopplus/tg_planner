import { Context } from 'grammy'

/**
 * /help — справка по командам
 */
export async function handleHelp(ctx: Context) {
  await ctx.reply(
    '📋 **TG Planer — Справка**\n\n' +
      '**Создание задач:**\n' +
      'Напиши текст или отправь голосовое — я распознаю задачи, даты и приоритет.\n' +
      'Можно несколько задач в одном сообщении!\n\n' +
      '**Команды:**\n' +
      '/today — ☀️ Мой день (автоматический план)\n' +
      '/tasks — все активные задачи\n' +
      '/projects — управление проектами\n' +
      '/app — 📱 открыть Mini App\n' +
      '/help — эта справка\n\n' +
      '**Примеры ввода:**\n' +
      '• "купить молоко завтра"\n' +
      '• "важно: сдать отчёт до пятницы 18:00"\n' +
      '• "позвонить врачу на этой неделе"\n' +
      '• "купить хлеб и вынести мусор завтра"\n' +
      '• "каждый понедельник в 9:00 — планёрка"\n\n' +
      '🎤 Голосовые сообщения тоже работают!\n' +
      '🔁 Поддержка повторяющихся задач.',
    { parse_mode: 'Markdown' },
  )
}
