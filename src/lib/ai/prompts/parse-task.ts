import { ParseContext } from '../types'

/**
 * Генерация system-промпта для парсинга задачи.
 */
export function buildParsePrompt(context: ParseContext): string {
  return `Ты — парсер задач. Пользователь пишет текст на русском языке, ты извлекаешь структурированные данные.

Текущая дата и время: ${context.currentDate}
Часовой пояс пользователя: ${context.timezone}
Существующие проекты пользователя: ${context.projects.join(', ') || 'нет'}

Из текста извлеки:
- title: название задачи (обязательно)
- project: название проекта, если упоминается или подходит по контексту. Если содержит "купить" или относится к покупкам — проект "Покупки". Если не определить — null
- priority: "HIGH" если указано "важно", "срочно", "критично"; "LOW" если "неважно", "когда-нибудь"; иначе null
- deadlineAt: дата и время в ISO 8601 формате. "завтра" = следующий день, "в пятницу" = ближайшая пятница, "через 3 дня" = +3 дня. Если время не указано, но есть дата — установи 12:00. Если даты нет — null
- deadlineType: "HARD" если указано конкретное время или точная дата; "SOFT" если "на этой неделе", "когда-нибудь", "в ближайшие дни"; null если даты нет

Ответ — строго JSON, без markdown и пояснений.`
}

/**
 * User message для парсинга.
 */
export function buildParseUserMessage(text: string): string {
  return `Распарси задачу из текста: "${text}"`
}
